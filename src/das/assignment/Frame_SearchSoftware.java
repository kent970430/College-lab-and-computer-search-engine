    private void showbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showbtnActionPerformed
        // TODO add your handling code here:

        String line;
        String search_name = softwaretxt.getText();
        boolean inttt = isInteger(search_name);
        int flagg = 0;
        Pattern regex = Pattern.compile("[$&+,:;=\\\\?@#|/'<>.^*()%!-]");
        try {
            BufferedReader lab = new BufferedReader(new FileReader("software.txt"));
            while ((line = lab.readLine()) != null) {
                String[] labbs = line.split(",");
                String software = labbs[0];
                String labsID = labbs[1];
                String Block = labbs[2];
                String floor = labbs[3];
                String no_of_pc = labbs[5];

                if (software.equals(search_name)) { //correct ID
                    blocktxt.setText(Block);
                    blocktxt.setEnabled(false);
                    labidtxt.setText(labsID);
                    labidtxt.setEnabled(false);
                    leveltxt.setText(floor);
                    leveltxt.setEnabled(false);
                    noofcotxt.setText(no_of_pc);
                    noofcotxt.setEnabled(false);
                    flagg = 1;
                    //show_all_compp();
                    break;
                    //} else if (!labid.equals(search_name)) { //incorrect ID, does not exist
                    // flagg = 2;
                } else if (regex.matcher(softwaretxt.getText()).find()) { //special char
                    flagg = 3;
                    break;
                } else if (inttt) { //integer
                    flagg = 4;
                    break;
                } else {
                    flagg = 2;
                }

            }
            switch (flagg) {
                case 1:
                show_all_compp();
                break;
                case 2:
                if (softwaretxt.getText().length() >= 1) { //false
                    JOptionPane.showMessageDialog(null, "The software does not exist", "Please try again", JOptionPane.ERROR_MESSAGE);
                    rst();
                } else { //blank
                    JOptionPane.showMessageDialog(null, "Please key in the software", "Please try again", JOptionPane.ERROR_MESSAGE);
                    rst();
                }
                break;
                case 3:
                JOptionPane.showMessageDialog(null, "Search cannot contain special characters", "Please try again", JOptionPane.ERROR_MESSAGE);
                rst();
                break;
                case 4:
                JOptionPane.showMessageDialog(null, "Please key in the software in full name", "Please try again", JOptionPane.ERROR_MESSAGE);
                rst();
                break;
                default:
                break;
            }
            lab.close();
        } catch (Exception e) {
            e.printStackTrace();

        }
    }//GEN-LAST:event_showbtnActionPerformed

